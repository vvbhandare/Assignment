public class CopounInfo {

    private String copounName;
    private String copounIcon;
    private int offerID;
    private String copounLink;

    public String getCopounName() {
        return copounName;
    }

    public void setCopounName(String copounName) {
        this.copounName = copounName;
    }

    public String getCopounIcon() {
        return copounIcon;
    }

    public void setCopounIcon(String copounIcon) {
        this.copounIcon = copounIcon;
    }

    public int getOfferID() {
        return offerID;
    }

    public void setOfferID(int offerID) {
        this.offerID = offerID;
    }

    public String getCopounLink() {
        return copounLink;
    }

    public void setCopounLink(String copounLink) {
        this.copounLink = copounLink;
    }
}
